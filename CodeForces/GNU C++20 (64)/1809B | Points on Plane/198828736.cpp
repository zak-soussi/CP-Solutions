#include <bits/stdc++.h>
typedef long long ll;
const char nl='\n';
using namespace std;
int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin>>t;
    while (t--)
    {
        ll n;
        cin >> n;
        ll q = sqrtl (n - 1);
        cout << q << '\n';
    }
    return 0;
}
 
 